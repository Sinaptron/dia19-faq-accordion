function mostrarContenido() {
    var contenido = document.getElementById("contenidoOculto");
    var iconoImagen = document.getElementById("iconoImagen");

    if (contenido.style.display ==='none') {
      // Si el contenido está oculto, lo mostramos y cambiamos el icono
      contenido.style.display ='block';
      iconoImagen.src = "images/icon-minus.svg";
    } else {
      // Si el contenido está visible, lo ocultamos y restauramos el icono original
      contenido.style.display ='none';
      iconoImagen.src = "images/icon-plus.svg";
    }
  }

